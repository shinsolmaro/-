package kr.co.bit_camp.vo;

import java.util.Date;

public class Hopebook {
	private int hopebookNo;
	private String id;
	private String hopebookWriter;
	private String hopebookTitle;
	private Date hopebookDate;
	
	public int getHopebookNo() {
		return hopebookNo;
	}
	public void setHopebookNo(int hopebookNo) {
		this.hopebookNo = hopebookNo;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getHopebookWriter() {
		return hopebookWriter;
	}
	public void setHopebookWriter(String hopebookWriter) {
		this.hopebookWriter = hopebookWriter;
	}
	public String getHopebookTitle() {
		return hopebookTitle;
	}
	public void setHopebookTitle(String hopebookTitle) {
		this.hopebookTitle = hopebookTitle;
	}
	public Date getHopebookDate() {
		return hopebookDate;
	}
	public void setHopebookDate(Date hopebookDate) {
		this.hopebookDate = hopebookDate;
	}
	

	 

}
