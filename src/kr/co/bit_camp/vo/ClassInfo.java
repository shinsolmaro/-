package kr.co.bit_camp.vo;

public class ClassInfo {
	private int classInfoNo;
	private String classInfoName;
	
	public int getClassInfoNo() {
		return classInfoNo;
	}
	public void setClassInfoNo(int classInfoNo) {
		this.classInfoNo = classInfoNo;
	}
	public String getClassInfoName() {
		return classInfoName;
	}
	public void setClassInfoName(String classInfoName) {
		this.classInfoName = classInfoName;
	}

}
